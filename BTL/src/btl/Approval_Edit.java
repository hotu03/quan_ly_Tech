    public void displayDetails(int possition, String hoTen, String dienThoai, String serial, String yeuCau, String trangThai, String technician, String dienThoaiTechnician, String ghiChu) {
        // Set the text of your JLabels or JTextFields in this method
        txtTenChuMay.setText(hoTen);
        txtSerial.setText(dienThoai);
        txtSerial.setText(serial);
        txtYeuCau.setText(yeuCau);
        if (trangThai.equalsIgnoreCase("Nhận máy")) {
            radNhanMay.setSelected(true);
        } else if (trangThai.equalsIgnoreCase("Hoàn thành")) {
            radHoanThanh.setSelected(true);
        } else if (trangThai.equalsIgnoreCase("Đang làm")) {
            radDangLam.setSelected(true);
        } else if (trangThai.equalsIgnoreCase("Đã trả máy")) {
            radDaTraMay.setSelected(true);
        }
        txtTech.setText(technician);
        txtSDTTech.setText(dienThoaiTechnician);
        txtGhiChu.setText(ghiChu);
        statements = trangThai;
        index = possition;
        serialNum = serial;
    }

