    private void bntCompleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntCompleteActionPerformed
        // TODO add your handling code here:

        String trangThai = "";
        if (radNhanMay.isSelected()) {
            trangThai = "Nhận máy";
        } else if (radHoanThanh.isSelected()) {
            trangThai = "Hoàn thành";
        } else if (radDangLam.isSelected()) {
            trangThai = "Đang làm";
        } else if (radDaTraMay.isSelected()) {
            trangThai = "Đã trả máy";
        }
        try {
            if (trangThai.equals(statements)) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn trạng thái khác để thực hiện chỉnh sửa hóa đơn", "Lỗi", JOptionPane.OK_OPTION);
                throw new Exception("Loi chua chinh sua");
            } else {
                List<Bill> billList = controller.readDataFromFile("src/file/bill.txt");
                billList.get(index).setStatements(trangThai);
                controller.writeToFile(billList, "src/file/bill.txt");
                List<Machine> listMachine = controller.readDataFromFile("src/file/machine.txt");
                for (Machine machine : listMachine) {
                    if (machine.getSerial().equals(serialNum)) {
                        machine.setStatements(trangThai);
                    }
                }
                controller.writeToFile(listMachine, "src/file/machine.txt");
                JOptionPane.showMessageDialog(this, "Chỉnh sửa hóa đơn thành công", "Thông báo", JOptionPane.OK_OPTION);
                new Approval_Screen().setVisible(true);
                this.dispose();
            }
        }
        catch(Exception ex){
            ex.printStackTrace();
        }


    }//GEN-LAST:event_bntCompleteActionPerformed

